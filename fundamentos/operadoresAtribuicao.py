# Operadores de Atribuição
a = 3
a = a + 7
print(a)

# Atribuição Aditiva
# a = a + 5 (lembrando que o valor anterior era igual a '10')
a += 5
print(a)

# Atribuição Subtrativa
a -= 3
print(a)

# Atribuição Multiplicativa
a *= 2
print(a)

# Atribuição Divisiva
a /= 4
print(a)

# Atribuição Modulativa
a %= 4
print(a)

# Atribuição Exponenciativa
a **= 8
print(a)

# Tem-se como fazer uma 'Divisão por Inteiro'
a //= 256
print(a)
