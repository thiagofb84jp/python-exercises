"""
6. Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.
"""
# Import math Library
import math

# Como chamar o valor de PI dentro do Python?
# Como declarar uma constante em Python?

raio = 3.6

area = math.pi * math.pow(raio, 2)

print('A área do círculo cujo raio é {} é de {:.2f} cm².'.format(raio, area))
