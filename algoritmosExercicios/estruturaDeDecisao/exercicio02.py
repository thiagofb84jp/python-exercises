"""
2. Faça um Programa que peça um valor e mostre na tela se o valor é positivo
    ou negativo.
"""

numero = 0

if (numero > 0):
    print("O número informado ({}) é positivo".format(numero))
elif (numero == 0):
    print("O número informado é zero.")
else:
    print("O número informado ({}) é negativo.".format(numero))
