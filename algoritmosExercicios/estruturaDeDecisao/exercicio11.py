"""
11. Faça um Programa que leia um número e exiba o dia
        correspondente da semana. (1-Domingo, 2- Segunda, etc.),
            se digitar outro valor deve aparecer valor inválido.
"""

diaDaSemana = 1

if diaDaSemana == 1:
    print("1 - Domingo")
elif diaDaSemana == 2:
    print("2 - Segunda")
elif diaDaSemana == 3:
    print("3 - Terça")
elif diaDaSemana == 4:
    print("4 - Quarta")
elif diaDaSemana == 5:
    print("5 - Quinta")
elif diaDaSemana == 6:
    print("6 - Sexta")
elif diaDaSemana == 7:
    print("7 - Sábado")
else:
    print("Valor Inválido! Por favor, informe outro.")
